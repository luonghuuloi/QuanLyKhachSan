public class DoanhThuThang {
    private String maDoanhThuThang;
    private String maNhanVien;
    private int Thang;
    private int Nam;
    private double tongDoanhThuNgay;
    private String ghiChu;

    public String getMaDoanhThuThang() {
        return maDoanhThuThang;
    }

    public void setMaDoanhThuThang(String maDoanhThuThang) {
        this.maDoanhThuThang = maDoanhThuThang;
    }

    public String getMaNhanVien() {
        return maNhanVien;
    }

    public void setMaNhanVien(String maNhanVien) {
        this.maNhanVien = maNhanVien;
    }

    public int getThang() {
        return Thang;
    }

    public void setThang(int Thang) {
        this.Thang = Thang;
    }

    public int getNam() {
        return Nam;
    }

    public void setNam(int Nam) {
        this.Nam = Nam;
    }

    public double getTongDoanhThuNgay() {
        return tongDoanhThuNgay;
    }

    public void setTongDoanhThuNgay(double tongDoanhThuNgay) {
        this.tongDoanhThuNgay = tongDoanhThuNgay;
    }

    public String getGhiChu() {
        return ghiChu;
    }

    public void setGhiChu(String ghiChu) {
        this.ghiChu = ghiChu;
    }

    
    
}
